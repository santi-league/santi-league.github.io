def extract_latest_date(files):
    """ä»æ–‡ä»¶ååˆ—è¡¨ä¸­æå–æœ€æ–°æ—¥æœŸ"""
    dates = []
    for fp in files:
        filename = os.path.basename(fp)
        # åŒ¹é…æ ¼å¼ï¼šæœˆ_æ—¥_å¹´
        match = re.match(r'(\d+)_(\d+)_(\d+)', filename)
        if match:
            month, day, year = match.groups()
            try:
                date_obj = datetime(int(year), int(month), int(day))
                dates.append(date_obj)
            except ValueError:
                continue

    if dates:
        latest_date = max(dates)
        return latest_date.strftime("%Yå¹´%mæœˆ%dæ—¥")
    return None


def extract_recent_games(files, results, count=5):
    """
    æå–æœ€è¿‘çš„Nä¸ªç‰Œè°±ä¿¡æ¯

    è¿”å›æ ¼å¼: [
        {
            'date': '2025å¹´1æœˆ15æ—¥',
            'players': ['ç©å®¶1', 'ç©å®¶2', 'ç©å®¶3', 'ç©å®¶4'],
            'ranks': [1, 2, 3, 4],  # å¯¹åº”æ¯ä¸ªç©å®¶çš„åæ¬¡
            'scores': [30000, 25000, 20000, 15000]  # å¯¹åº”æ¯ä¸ªç©å®¶çš„æœ€ç»ˆç‚¹æ•°
        },
        ...
    ]
    """
    # åˆ›å»ºæ–‡ä»¶å’Œç»“æœçš„æ˜ å°„
    file_result_pairs = []
    for fp, result in zip(files, results):
        filename = os.path.basename(fp)
        # åŒ¹é…æ ¼å¼ï¼šæœˆ_æ—¥_å¹´
        match = re.match(r'(\d+)_(\d+)_(\d+)', filename)
        if match:
            month, day, year = match.groups()
            try:
                date_obj = datetime(int(year), int(month), int(day))
                file_result_pairs.append((date_obj, fp, result))
            except ValueError:
                continue

    # æŒ‰æ—¥æœŸé™åºæ’åº
    file_result_pairs.sort(key=lambda x: x[0], reverse=True)

    # æå–æœ€è¿‘çš„Nä¸ªç‰Œè°±
    recent_games = []
    for date_obj, fp, result in file_result_pairs[:count]:
        summary = result.get('summary', [])

        # æå–ç©å®¶ä¿¡æ¯
        players = []
        ranks = []
        scores = []

        # æŒ‰åæ¬¡æ’åº
        sorted_summary = sorted(summary, key=lambda x: x.get('rank', 99))

        for player_data in sorted_summary:
            players.append(player_data.get('name', 'æœªçŸ¥'))
            ranks.append(player_data.get('rank', 0))
            scores.append(player_data.get('final_points', 0))

        recent_games.append({
            'date': date_obj.strftime("%Yå¹´%mæœˆ%dæ—¥"),
            'date_en': date_obj.strftime("%Y-%m-%d"),
            'players': players,
            'ranks': ranks,
            'scores': scores
        })

    return recent_games


